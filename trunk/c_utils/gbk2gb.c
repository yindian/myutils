#include <stdio.h>

typedef unsigned short int wchar_gbk;

typedef struct {
    wchar_gbk fan;
    wchar_gbk jian;
} charmap_pair;

#define NUM_CHARS 5332
charmap_pair charmap[NUM_CHARS];

wchar_gbk gbk_f2j(wchar_gbk in)
{
    int low=0,high=NUM_CHARS-1,mid;
    wchar_gbk wc_gb_mid, out = in;

    while (low<=high)
    {
        mid = (low+high)/2;
        wc_gb_mid = charmap[mid].fan;
        if (in == wc_gb_mid)
        {
            out = charmap[mid].jian;
            break;
        }
        else
        {
            if (in < wc_gb_mid)
            {
                high = mid - 1;
            }
            else
            {
                low = mid + 1;
            }
        }
    }

    return out;
}

int main(int argc, char** argv)
{
    char ch;
    wchar_gbk wc_gb;

    ch = fgetc(stdin);
    while ( ch != EOF )
    {
        if (ch < 0)  /* 对GBK字符 */
        {
            wc_gb = (ch << 8) + fgetc(stdin);
            ch = fgetc(stdin);

            wc_gb = gbk_f2j(wc_gb);
            fputc((wc_gb >> 8), stdout);
            fputc((wc_gb & 0xFF), stdout);
        }
        else /* 对ASCII字符,直接输出 */
        {
            fputc(ch, stdout);
            ch = fgetc(stdin);
        }
    }
    return 0;
}

