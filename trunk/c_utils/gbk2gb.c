#include <stdio.h>

typedef unsigned short int wchar_gbk;

typedef struct {
    wchar_gbk fan;
    wchar_gbk jian;
} charmap_pair;

typedef struct {
    wchar_gbk fan;
    char *    jian;
} charmap2_pair;

#define NUM_CHARS 4583
#define NUM_CHARS2 75
charmap_pair charmap[];
charmap2_pair charmap2[];

wchar_gbk gbk_f2j(wchar_gbk in)
{
    int low=0,high=NUM_CHARS-1,mid;
    wchar_gbk wc_gb_mid, out = in;

    while (low<=high)
    {
        mid = (low+high)/2;
        wc_gb_mid = charmap[mid].fan;
        if (in == wc_gb_mid)
        {
            out = charmap[mid].jian;
            break;
        }
        else
        {
            if (in < wc_gb_mid)
            {
                high = mid - 1;
            }
            else
            {
                low = mid + 1;
            }
        }
    }

    return out;
}

char * gbk_f2j_2(wchar_gbk in)
{
    int low=0,high=NUM_CHARS2-1,mid;
    wchar_gbk wc_gb_mid;
    char * out = NULL;

    while (low<=high)
    {
        mid = (low+high)/2;
        wc_gb_mid = charmap2[mid].fan;
        if (in == wc_gb_mid)
        {
            out = charmap2[mid].jian;
            break;
        }
        else
        {
            if (in < wc_gb_mid)
            {
                high = mid - 1;
            }
            else
            {
                low = mid + 1;
            }
        }
    }

    return out;
}

int main(int argc, char** argv)
{
    char ch;
    wchar_gbk wc_gbk, wc_gb;
    char * s;

    ch = fgetc(stdin);
    while ( ch != EOF )
    {
        if (ch < 0)  /* 对GBK字符 */
        {
            wc_gbk = (ch << 8) + fgetc(stdin);
            ch = fgetc(stdin);

            wc_gb = gbk_f2j(wc_gbk);
            if (wc_gb == wc_gbk)
                s = gbk_f2j_2(wc_gbk);
            else
                s = NULL;
            if (s)
                fputs(s, stdout);
            else
            {
                fputc((wc_gb >> 8), stdout);
                fputc((wc_gb & 0xFF), stdout);
            }
        }
        else /* 对ASCII字符,直接输出 */
        {
            fputc(ch, stdout);
            ch = fgetc(stdin);
        }
    }
    return 0;
}

