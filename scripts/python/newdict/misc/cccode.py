def prn(*args, **kwargs):
    args = list(args)
    for i in range(len(args)):
        s = args[i]
        if type(s) != str:
            try:
                args[i] = s.encode('utf-8')
            except:
                pass
    print(*args, **kwargs)

def _check_cn_ccc():
    visited = set()
    for line in _tab_cn_ccc.splitlines():
        ar = line.split('\t')
        try:
            assert len(ar) == 2
            n = int(ar[0])
            if ar[1] not in code2hanzi[n]:
                #prn(line, ','.join(code2hanzi.get(n)))
                code2hanzi[n].append(ar[1])
            else:
                code2hanzi[n].append(ar[1])
            visited.add(n)
        except:
            prn(line, file=sys.stderr)
            raise
    for n in range(1, 10000):
        if not n in visited:
            code2hanzi[n].append('')

