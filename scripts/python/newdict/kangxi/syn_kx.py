for c in syn.keys():
	s = set(syn[c].split('|'))
	if c not in s:
		syn[c] = c+'|'+syn[c]
	else:
		s.remove(c)
		syn[c] = c+'|'+'|'.join(s)

try:
	f = open('kangxi_gaiji.txt', 'r')
	for line in f:
		if line.startswith('#'):
			continue
		line = line[:-1].split('\t')
		assert len(line) in (2, 3)
		if not line[0] or (len(line) == 3 and line[2] == '×'):
			continue
		c = line[0]
		if syn.has_key(c):
			s = set(syn[c].split('|'))
			if line[1] not in s:
				syn[c] = syn[c]+'|'+line[1]
		else:
			syn[c] = c+'|'+line[1]
	f.close()
except IOError:
	pass

ss = set(syn.keys())

n = 0
for line in sys.stdin:
	p = line.find('\t')
	if syn.has_key(line[:p]):
		sys.stdout.write(syn[line[:p]])
		sys.stdout.write(line[p:])
		ss.remove(line[:p])
		n += 1
	else:
		sys.stdout.write(line)

print >> sys.stderr, 'n =', n, '  len(syn) =', len(syn), ' ss =', ss
for c in ss:
	print >> sys.stderr, c, '->', syn[c]
assert n == len(syn)
