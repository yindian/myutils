/*
 * Unicode Fanti to Jianti converter
 * Read in utf-8 text stream and convert the Traditional Chinese
 * characters to Simplified Chinese characters.
 */
#include<stdio.h>
#include<stdlib.h>
#include<assert.h>
typedef long wchar_utf8;
#define ILLEGAL_WCHAR_UTF8 -1
#define MAXBUF 16
typedef struct {
    wchar_utf8 fan;
    wchar_utf8 jian;
} charmap_pair;
#define NUM_CHARS 5332
static const charmap_pair charmap[NUM_CHARS];
static const char trailingBytesForUTF8[256];

wchar_utf8 getwchar_utf8(char *buf, FILE *fp)
{
	wchar_utf8 wch;
	int ch, extrabytes;
	ch = fgetc(fp);
	if (ch == EOF)
		return EOF;
	extrabytes = trailingBytesForUTF8[ch];
	*buf++ = ch;
	wch = ((1 << (6 - extrabytes)) - 1) & ch;
	while (--extrabytes >= 0)
	{
		ch = fgetc(fp);
		assert(ch != EOF);
		*buf++ = ch;
		wch = (wch << 6) | (ch & 0x3F);
	}
	*buf = '\0';
	return wch;
}

char *code2utf(wchar_utf8 code, char buf[])
{
	char *q;
	q = buf;
	if (code <= 0x0000007f)
		*q++ = code;
	else if (code <= 0x000007ff)
	{
		*q++ = 0xc0 | (code >> 6);
		*q++ = 0x80 | (code & 0x3f);
	}
	else if (code <= 0x0000ffff)
	{
		*q++ = 0xe0 | (code >> 12);
		*q++ = 0x80 | ((code >> 6) & 0x3f) ;
		*q++ = 0x80 | (code & 0x3f);
	}
	else if (code <= 0x001fffff)
	{
		*q++ = 0xf0 | (code >> 18);
		*q++ = 0x80 | ((code >> 12) & 0x3f) ;
		*q++ = 0x80 | ((code >> 6) & 0x3f) ;
		*q++ = 0x80 | (code & 0x3f);
	}
	else if (code <= 0x03ffffff)
	{
		*q++ = 0xf8 | (code >> 24);
		*q++ = 0x80 | ((code >> 18) & 0x3f) ;
		*q++ = 0x80 | ((code >> 12) & 0x3f) ;
		*q++ = 0x80 | ((code >> 6) & 0x3f) ;
		*q++ = 0x80 | (code & 0x3f);
	}
	else if (code <= 0x7fffffff)
	{
		*q++ = 0xfc | (code >> 30);
		*q++ = 0x80 | ((code >> 24) & 0x3f) ;
		*q++ = 0x80 | ((code >> 18) & 0x3f) ;
		*q++ = 0x80 | ((code >> 12) & 0x3f) ;
		*q++ = 0x80 | ((code >> 6) & 0x3f) ;
		*q++ = 0x80 | (code & 0x3f);
	}
	else
		assert(code <= 0x7fffffff);
	*q = '\0';
	return buf;
}

wchar_utf8 utf8_f2j(wchar_utf8 in)
{
    int low, high, mid;
    wchar_utf8 wch_mid, out;
    out = in;
    for (low = 0, high=NUM_CHARS-1; low<=high; )
    {
        mid = (low+high)/2;
        wch_mid = charmap[mid].fan;
        if (in == wch_mid)
        {
            out = charmap[mid].jian;
            break;
        }
	if (in < wch_mid)
	{
		high = mid - 1;
	}
	else
	{
		low = mid + 1;
	}
    }
    return out;
}

int main()
{
	char buf[MAXBUF];
	char ch;
	wchar_utf8 wch;
	while ((ch = getchar()) != EOF)
	{
		if (ch >= 0)
			putchar(ch);
		else
		{
			ungetc(ch, stdin);
			wch = getwchar_utf8(buf, stdin);
			wch = utf8_f2j(wch);
			printf("%s", code2utf(wch, buf));
		}
	}
	return 0;
}

