#include <stdio.h>
#include <malloc.h>

typedef unsigned short int wchar_gbk;

typedef struct {
    wchar_gbk fan;
    wchar_gbk jian;
    char *    jian_alt;
} charmap_pair;

#define NUM_CHARS 4716
charmap_pair charmap[];

wchar_gbk gbk_f2j(wchar_gbk in, char **alt)
{
    int low=0,high=NUM_CHARS-1,mid;
    wchar_gbk wc_gb_mid, out = in;

    *alt = NULL;
    while (low<=high)
    {
        mid = (low+high) >> 1;
        wc_gb_mid = charmap[mid].fan;
        if (in == wc_gb_mid)
        {
            out = charmap[mid].jian;
            *alt = charmap[mid].jian_alt;
            break;
        }
        else
        {
            if (in < wc_gb_mid)
            {
                high = mid - 1;
            }
            else
            {
                low = mid + 1;
            }
        }
    }

    return out;
}
int main(int argc, char** argv)
{
    int ch;
    wchar_gbk wc_gbk, wc_gb;
    char * s;
#define OBUFSIZE 4096
    char * stdobuf;

    stdobuf = malloc(OBUFSIZE);
    setvbuf(stdout, stdobuf, stdobuf != NULL ? _IOFBF : _IONBF, OBUFSIZE);

    ch = getc(stdin);
    while ( ch != EOF )
    {
        if (ch > 0x80)  /* 对GBK字符 */
        {
            wc_gbk = (ch << 8) + getc(stdin);
            ch = getc(stdin);

            wc_gb = gbk_f2j(wc_gbk, &s);
            if (s)
                fputs(s, stdout);
            else
            {
                putchar(wc_gb >> 8);
                putchar(wc_gb & 0xFF);
            }
        }
        else /* 对ASCII字符,直接输出 */
        {
            putchar(ch);
            if (ch == '\n')
                fflush(stdout);
            ch = getc(stdin);
        }
    }
    fflush(stdout);
    return 0;
}

